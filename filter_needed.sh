#!/bin/bash
RAW=$($1 $2 \
| sed "s|[^ ]*.o: ||" \
| sed -r "s|([^ ]*/[^ ]*)\.cpp|$3/\1\.o|g" \
| sed -r "s|[^ ]*/([^ ]*)\.hpp|$3/\1\.o|g")

RAW=$(echo $RAW | sed -r "s|(([^ ]*/[^ ]*\.o) .*)\2|\1|g")

echo $RAW